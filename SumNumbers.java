public class SumNumbers {
    public static int sumNumbers(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        System.out.println(sumNumbers(3, 5));  // Output: 8
        System.out.println(sumNumbers(-2, 7)); // Output: 5
        System.out.println(sumNumbers(0, 0));  // Output: 0
    }
}
